#One Value to Multiple Variables in same line
x = y = z = "Orange"
print(x)
print(y)
print(z)
#Many Values to Multiple Variables
#Python allows you to assign values to multiple variables in one line:
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)
# To Unpack a list

fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)